package Persona;

public class Spaceship {
    int capacity;
    int speed;
    int safetyRating;

    public int getCapacity() {
        return capacity;
    }

    public int getSpeed() {
        return speed;
    }

    public int getSafetyRating(){
        return safetyRating;
    }

    public void setCapacity(int cap) {
        capacity = cap;
    }

    public void setSpeed(int speed) {
        speed = speed;
    }

    public void setSafetyRating(int safety){
        safetyRating = safety;
    }

}
